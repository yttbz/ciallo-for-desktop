/**
 * Resolve a dot-separated key against a locale object.
 * Falls back through: targetLang -> en -> raw key.
 */
function resolveKey(locale, key) {
  if (locale && Object.prototype.hasOwnProperty.call(locale, key)) {
    return locale[key];
  }
  // Try English fallback
  if (LOCALES.en && Object.prototype.hasOwnProperty.call(LOCALES.en, key)) {
    return LOCALES.en[key];
  }
  // Return raw key as last resort
  return key;
}

/**
 * Create a translator function bound to a language getter.
 *
 * @param {Function} getLangFn - A function that returns the current language code (e.g., 'zh-CN', 'en', 'ja', 'ko')
 * @returns {Function} t(key) - Translation function
 */
function createTranslator(getLangFn) {
  /**
   * Translate a dot-separated key to the current language.
   *
   * @param {string} key - Translation key (e.g., 'session.working', 'settings.language')
   * @param {Object} [interpolations] - Optional key-value pairs for string interpolation using {{key}} syntax
   * @returns {string} Translated string
   *
   * @example
   * const t = createTranslator(() => 'zh-CN');
   * t('session.working')             // => '工作中'
   * t('elapsed', { val: '5m' })      // => '已用时间: 5m'  (if locale string contains '{{val}}')
   */
  return function t(key, interpolations) {
    const lang = typeof getLangFn === 'function' ? getLangFn() : 'en';
    const locale = LOCALES[lang];
    let value = resolveKey(locale, key);

    // Apply string interpolation
    if (interpolations && typeof interpolations === 'object') {
      for (const [k, v] of Object.entries(interpolations)) {
        value = String(value).replace(new RegExp(`\\{\\{\\s*${k}\\s*\\}}`, 'g'), String(v));
      }
    }

    return value;
  };
}

